// 1. Trees are hierarchical data structures that consist of nodes connected by edges.
// 2. Each tree has a root node, which is the topmost node in the hierarchy.
// 3. Nodes in a tree can have zero or more child nodes.
// 4. The node that is directly above a given node is called its parent node.
// 5. Nodes that have the same parent node are called siblings.
// 6. The depth of a node in a tree is the number of edges from the root node to that node.
// 7. The height of a tree is the maximum depth of any node in the tree.
// 8. Trees can be classified into different types, such as binary trees, AVL trees, B-trees, etc., based on their specific properties and rules.

// Binary Tree:
// A binary tree is a tree in which each node has at most two children, which are referred to as the left child and the right child.

// Binary Search Tree:
// A binary search tree is a binary tree in which all the nodes follow the below-mentioned properties:
// 1. The left sub-tree of a node has a key less than or equal to its parent node's key.
// 2. The right sub-tree of a node has a key greater than to its parent node's key.
// In this way, the binary search tree provides efficient sorting and searching of data.
